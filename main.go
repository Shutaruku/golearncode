package main

import "fmt"

func main() {
	var myStr string
	fmt.Println(myStr)
	myStr = "myString"
	fmt.Println(myStr)

	var myInt int
	fmt.Println(myInt)
	myInt = 8
	fmt.Println(myInt)

	var myFloat float64
	fmt.Println(myFloat)
	myFloat = 2.3
	fmt.Println(myFloat)

	var myBool bool
	fmt.Println(myBool)
	myBool = true
	fmt.Println(myBool)
}
